can.Model.extend("Sentrana.Models.AuthenticationModel", {
    login: function(url, params, success, error) {
        $.ajax({
            url: url,
            type: 'POST',
            contentType: "application/json; charset=utf-8",
            dataType: 'json',
            async: true,
            data: params,
            success: success,
            error: error
        });
    },

    checkLogin: function(url, params, success, error) {
        $.ajax({
            url: url,
            type: 'GET',
            dataType: 'json',
            async: false,
            success: success,
            error: error
        });
    },

    logout: function(url, success, error) {
        $.ajax({
            url: url,
            type: 'GET',
            dataType: 'json',
            async: true,
            success: success,
            error: error
        });
    },

    resetPassword: function (url, success, error, complete) {
        $.ajax({
            url: url,
            type: 'GET',
            contentType: "application/json; charset=utf-8",
            dataType: 'json',
            async: true,
            complete: complete,
            success: success,
            error: error
        });
    },

    changePassword: function(url, params, success, error) {
        $.ajax({
           url: url,
           type: 'POST',
           contentType: "application/json; charset=utf-8",
           dataType: 'json',
           async: true,
           data: JSON.stringify(params),
           success: success,
           error: error
        });
   },

   isValidToken: function (url, success, error) {
       $.ajax({
           url: url,
           type: 'GET',
           dataType: 'json',
           async: true,
           success: success,
           error: error
       });
   },

   changeAutoGeneratedPassword: function (url, params, success, error, complete) {
       $.ajax({
           url: url,
           type: 'POST',
           contentType: "application/json; charset=utf-8",
           dataType: 'json',
           async: true,
           data: JSON.stringify(params),
           success: success,
           error: error,
           complete: complete
       });
   }
}, {
});
